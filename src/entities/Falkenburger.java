package entities;

public class Falkenburger {
	private String alves;
	private Integer numero;
	public Falkenburger(String alves, Integer numero) {
		super();
		this.alves = alves;
		this.numero = numero;
	}
	public String getAlves() {
		return alves;
	}
	public void setAlves(String alves) {
		this.alves = alves;
	}
	public Integer getNumero() {
		return numero;
	}
	public void setNumero(Integer numero) {
		this.numero = numero;
	}
	
	
	

}
